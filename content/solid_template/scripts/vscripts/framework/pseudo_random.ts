/** @private */
function PfromC(c: number) {
	if (c == 0) return 1;
	let pProcOnN = 0;
	let pProcByN = 0;
	let sumNpProcOnN = 0;
	let maxFails = math.ceil(1 / c);
	for (const N of $range(1, maxFails, 1)) {
		pProcOnN = math.min(1, N * c) * (1 - pProcByN);
		pProcByN = pProcByN + pProcOnN;
		sumNpProcOnN = sumNpProcOnN + N * pProcOnN;
	}
	return 1 / sumNpProcOnN;
}

/** @private */
function CfromP(p: number) {
	let Cupper = p;
	let Clower = 0;
	let Cmid;
	let p1;
	let p2 = 1;
	while (true) {
		Cmid = (Cupper + Clower) / 2;
		p1 = PfromC(Cmid);
		if (math.abs(p1 - p2) <= 0) break;
		if (p1 > p) {
			Cupper = Cmid;
		} else {
			Clower = Cmid;
		}
		p2 = p1;
	}
	return Cmid;
}
let PRD_ratio = 2;
let PRD_factor = math.pow(10, PRD_ratio);
/** @private */
function PRD_C(chance: number) {
	chance = Round(Clamp(chance, 0, 100) * PRD_factor);
	return PSEUDO_RANDOM_C[chance - 1] ?? 0;
}
/**
 * 伪随机
 * @param table 对象
 * @param chance 概率，必须是整数
 * @param pseudo_random_recording 记录索引
 */
function PRD(table: any, chance: number, pseudo_random_recording: string) {
	if (typeof table != "object") return false;
	if (table.PSEUDO_RANDOM_RECORDING_LIST == undefined) table.PSEUDO_RANDOM_RECORDING_LIST = {};

	if (chance <= 0) {
		return false;
	}
	let C = PRD_C(chance);
	if (C == 0) {
		return false;
	}
	let N = table.PSEUDO_RANDOM_RECORDING_LIST[pseudo_random_recording] ?? 1;
	if (math.random() > C * N) {
		table.PSEUDO_RANDOM_RECORDING_LIST[pseudo_random_recording] = N + 1;
		return false;
	}
	table.PSEUDO_RANDOM_RECORDING_LIST[pseudo_random_recording] = 1;
	return true;
}

